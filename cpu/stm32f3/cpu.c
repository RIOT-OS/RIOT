#include "cpu.h"
#include "periph/init.h"
#include "stmclk.h"

/**
 * @brief Initialize the CPU, set IRQ priorities
 */
void cpu_init(void)
{
    /* initialize the Cortex-M core */
    cortexm_init();
    /* initialize the clock system */
    stmclk_init_sysclk();
    /* trigger static peripheral initialization */
    periph_init();
}
