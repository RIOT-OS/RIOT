static void test_hmac_sha256(const uint8_t *key, size_t key_len,
                             const uint8_t *data, size_t data_len,
                             const uint8_t *result, size_t result_len,
                             char *message)
{
    uint8_t hmac[PSA_HASH_LENGTH(PSA_ALG_SHA_256)];
    psa_status_t status;
    size_t hmac_length;

    assert(result_len <= sizeof(hmac));

    if (result_len > sizeof(hmac)) {
        TEST_FAIL("Result is too large to be checked");
    }

    status = psa_crypto_init();
    if (status != PSA_SUCCESS) {
        TEST_FAIL("PSA initialization failed");
    }

    psa_mac_operation_t operation = psa_mac_operation_init();
    psa_key_attributes_t attributes = psa_key_attributes_init();
    psa_key_id_t key_handle = 0;

    psa_set_key_algorithm(&attributes, PSA_ALG_HMAC(PSA_ALG_SHA_256));
    psa_set_key_usage_flags(&attributes, PSA_KEY_USAGE_SIGN_MESSAGE);
    psa_set_key_bits(&attributes, PSA_BYTES_TO_BITS(key_len));
    psa_set_key_type(&attributes, PSA_KEY_TYPE_HMAC);

    status = psa_import_key(&attributes, key, key_len, &key_handle);
    if (status != PSA_SUCCESS) {
        psa_destroy_key(key_handle);
        TEST_FAIL("Key import failed");
    }

    status = psa_mac_sign_setup(&operation, key_handle, PSA_ALG_HMAC(PSA_ALG_SHA_256));
    if (status != PSA_SUCCESS) {
        psa_destroy_key(key_handle);
        TEST_FAIL("MAC setup failed");
    }

    status = psa_mac_update(&operation, data, data_len);
    if (status != PSA_SUCCESS) {
        psa_destroy_key(key_handle);
        TEST_FAIL("MAC update failed");
    }

    status = psa_mac_sign_finish(&operation, hmac, sizeof(hmac), &hmac_length);
    if (status != PSA_SUCCESS) {
        psa_destroy_key(key_handle);
        TEST_FAIL("MAC finishing failed");
    }

    psa_destroy_key(key_handle);

    TEST_ASSERT_MESSAGE(hmac_length == sizeof(hmac), "HMAC result size mismatch");
    /* Using result_len is important to allow for truncating the checked data */
    TEST_ASSERT_MESSAGE(memcmp(hmac, result, result_len) == 0, message);
}

