#include <stdio.h>

#include "shell.h"

int main(void)
{
    puts("NDN-over-BLE with autoconn BLE connection manager");

    /* start shell */
    puts("All up, running the shell now");
    char line_buf[SHELL_DEFAULT_BUFSIZE];
    shell_run(NULL, line_buf, SHELL_DEFAULT_BUFSIZE);

    /* should never be reached */
    return 0;
}
